        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        int3 DecodeCellIndices(byte idx){
            int3 value = new int3();
            value.z = -((idx >> 2) & 1);
            value.y = -((idx >> 1) & 1);
            value.x = -(idx & 1);
            return value;
        }
